import UIKit

extension UIFont {
    
    // Avenir Next
    
    static func avenirNext30() -> UIFont? {
        return UIFont.init(name: "Avenir Next", size: 30)
    }
    static func avenirNext20() -> UIFont? {
        return UIFont.init(name: "Avenir Next", size: 20)
    }
    static func avenirNextDemiBold30() -> UIFont? {
        return UIFont.init(name: "Avenir Next Demi Bold", size: 30)
    }
    static func avenirNextDemiBold32() -> UIFont? {
        return UIFont.init(name: "Avenir Next Demi Bold", size: 20)
    }
}
